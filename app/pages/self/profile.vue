<script setup>
  import { useAppConfig } from 'nuxt/app'
  import { ref, watch, onMounted, toRaw } from 'vue'
  import { useRoute, useRouter } from 'vue-router'
  import { Avatar, Tabs, Tab } from 'flowbite-vue'
  import { isDev } from '@/utils/helpers'
  import { useAuth } from '@/composables/auth'
  import { useNavigation } from '@/composables/navigation'

  const activeTab = ref('posts')

  const route = useRoute()
  const router = useRouter()
  const config = useAppConfig()

  const auth = useAuth()

  const hasError = ref(false)
  const showBlocks = ref(false)


  const userinfoInitial = {
    profile: {},
  }

  const userinfo = ref(userinfoInitial)

  // Each loading state
  const loadState = ref({
    profile: true,
  })




  onMounted(async () => {
    if (!auth.isLoggedIn()) {
      try {
        await auth.restoreSession()
      } catch (err) {
        const navi = useNavigation()
        if (!navi.navigate.value)
          navi.navigate = useNavigation({ next: null, prev: null })
        // Back to current page
        navi.setNext(route.name);
        return navigateTo('/sign-in')
      }
    }

    await loadProfile()

  })


  const loadProfile = async () => {
    userinfo.value = userinfoInitial
    hasError.value = false
    loadState.value.profile = false

    try {
      const result = await auth.getProfile()
      updateUserInfo('profile', result)

    } catch (err) {
      if (isDev()) console.error(err)
      hasError.value = true
      updateUserInfo('profile', {
        did: getDid() ?? 'error:unknown:unknown',
        displayName: getHandle() ?? 'Error: Unknown',
        avatar: '',
      })
    }
  }

  /**
   * update user datum
   * @param {string} item
   * @param {any} value
   */
  const updateUserInfo = (item, value) => {
    if (isDev()) console.log('[updateUserInfo]::', item, ' = ', value)
    userinfo.value[item] = value
    loadState.value[item] = true
  }


</script>

